INSERT INTO
    M_CHARGE (
        departure_station_cd,
        arrival_station_cd,
        train_type_cd,
        seat_type_cd,
        charge
    )
SELECT
    dep.station_cd AS departure_station_cd,
    arr.station_cd AS arrival_station_cd,
    tt.train_type_cd,
    st.seat_type_cd,
    ABS(dep.ord - arr.ord) * 1000 AS charge
FROM
    station_order dep
    JOIN station_order arr ON dep.station_cd <> arr.station_cd
    CROSS JOIN M_TRAIN_TYPE tt
    CROSS JOIN M_SEAT_TYPE st ON CONFLICT (
        departure_station_cd,
        arrival_station_cd,
        train_type_cd,
        seat_type_cd
    ) DO
